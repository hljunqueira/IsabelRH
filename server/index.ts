// üîê Rota de autentica√ß√£o com Supabase
app.get("/api/auth/me", async (req, res) => {
  console.log('üîê Auth/me: Endpoint acessado');
  
  try {
    // Verificar se existe token de autoriza√ß√£o
    const authHeader = req.headers.authorization;
    
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      console.log('‚ö†Ô∏è Auth/me: Sem token, retornando dados mock');
      // Fallback para dados mock se n√£o houver autentica√ß√£o
      const mockUser = {
        usuario: {
          id: "dev-user-1",
          email: "dev@isabelrh.com.br",
          name: "Usu√°rio de Desenvolvimento",
          type: "admin",
          created_at: new Date().toISOString()
        }
      };
      return res.json(mockUser);
    }

    const token = authHeader.substring(7); // Remove 'Bearer ' do in√≠cio
    
    // Verificar o token com o Supabase
    const { data: { user }, error } = await supabase.auth.getUser(token);
    
    if (error || !user) {
      console.log('‚ùå Auth/me: Token inv√°lido, usando fallback');
      // Fallback para dados mock se o token for inv√°lido
      const mockUser = {
        usuario: {
          id: "dev-user-1",
          email: "dev@isabelrh.com.br",
          name: "Usu√°rio de Desenvolvimento",
          type: "admin",
          created_at: new Date().toISOString()
        }
      };
      return res.json(mockUser);
    }

    // Buscar dados do usu√°rio nas tabelas customizadas
    const { data: userData, error: userError } = await supabase
      .from('users')
      .select('*')
      .eq('id', user.id)
      .single();

    if (userError) {
      console.log('‚ö†Ô∏è Auth/me: Usu√°rio n√£o encontrado nas tabelas, usando dados do auth');
      return res.json({
        usuario: {
          id: user.id,
          email: user.email,
          name: user.user_metadata?.name || user.email,
          type: user.user_metadata?.type || "candidato",
          created_at: user.created_at
        }
      });
    }

    console.log('‚úÖ Auth/me: Retornando dados do usu√°rio autenticado');
    res.json({
      usuario: {
        id: userData.id,
        email: userData.email,
        name: userData.name,
        type: userData.type,
        created_at: userData.created_at
      }
    });
    
  } catch (error) {
    console.error('üí• Erro na autentica√ß√£o:', error);
    // Fallback para dados mock em caso de erro
    const mockUser = {
      usuario: {
        id: "dev-user-1",
        email: "dev@isabelrh.com.br",
        name: "Usu√°rio de Desenvolvimento",
        type: "admin",
        created_at: new Date().toISOString()
      }
    };
    res.json(mockUser);
  }
});

